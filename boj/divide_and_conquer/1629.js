const fs = require('fs');
const path = process.platform === 'linux' ? '/dev/stdin' : 'input.txt';
const [a, b, c] = fs.readFileSync(path).toString().split(' ').map(BigInt);

function multiply(a, b, c) {
  if (b == 1n) {
    return a % c;
  }

  return b % 2n == 0
    ? multiply(a, b / 2n, c) ** 2n % c
    : (multiply(a, b / 2n, c) ** 2n * a) % c;
}


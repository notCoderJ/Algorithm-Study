def solution():
  n = int(input())
  k = int(input())
  cnt = lambda x: sum([min(n, x // i) for i in range(1, n + 1)])
  
  left = 1
  right = n * n
  while left < right:
    mid = (left + right) // 2
    if cnt(mid) < k:
      left = mid + 1
    else:
      right = mid
  print(left)

if __name__ == "__main__":
  solution()
